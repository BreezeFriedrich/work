package com.hysm.pojo;

public class Lock {
	private int lock_id,is_idcard,is_pwd,state,other;
	private String lock_name,data;
	
	
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public int getLock_id() {
		return lock_id;
	}
	public void setLock_id(int lock_id) {
		this.lock_id = lock_id;
	}
	public int getIs_idcard() {
		return is_idcard;
	}
	public void setIs_idcard(int is_idcard) {
		this.is_idcard = is_idcard;
	}
	public int getIs_pwd() {
		return is_pwd;
	}
	public void setIs_pwd(int is_pwd) {
		this.is_pwd = is_pwd;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public int getOther() {
		return other;
	}
	public void setOther(int other) {
		this.other = other;
	}
	public String getLock_name() {
		return lock_name;
	}
	public void setLock_name(String lock_name) {
		this.lock_name = lock_name;
	}
	
	
	
}
